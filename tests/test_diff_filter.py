def test_partition_files_drops_lockfile_and_generated():
    files = [
        {"filename": "src/foo.py", "patch": "+print('x')\n"},
        {"filename": "package-lock.json", "patch": "+{ }\n"},
        {"filename": "src/bar.py",
         "patch": "@@ -0,0 +1,1 @@\n+// @generated\n"},
        {"filename": "node_modules/foo.js", "patch": "+x\n"},
        {"filename": "src/keep.py", "patch": "+ok\n"},
    ]
    kept, skipped = partition_files(files)
    kept_names = {f["filename"] for f in kept}
    assert kept_names == {"src/foo.py", "src/keep.py"}
    assert set(skipped) == {
        "package-lock.json",
        "src/bar.py",
        "node_modules/foo.js",
    }


def test_partition_files_respects_custom_patch_key():
    files = [
        {"filename": "src/foo.py", "diff": "+x\n"},
        {"filename": "yarn.lock", "diff": "+y\n"},
    ]
    kept, skipped = partition_files(files, patch_key="diff")
    assert [f["filename"] for f in kept] == ["src/foo.py"]
    assert skipped == ["yarn.lock"]
